class Cpu{
  private:
    
    uint8_t A{}, X{}, Y{}, SP{0xFD}, J{}, K{};
    uint16_t PC{};

    struct {
      bool C,Z,I,D,B,EX,V,N;
      uint8_t pack() const {
      return C | (Z<<1) | (I<<2) | (D<<3) | (B<<4) | (EX<<5) | (V<<6) | (N<<7);
    }
    void unpack(uint8_t v){
      C = ((v & 0x1) != 0);
      Z = (((v >> 1) & 0x1) != 0);
      I = (((v >> 2) & 0x1) != 0);
      D = (((v >> 3) & 0x1) != 0);
      B = (((v >> 4) & 0x1) != 0);
      EX = (((v >> 5)& 0x1) != 0);
      V = (((v >> 6) & 0x1) != 0);
      N = (((v >> 7) & 0x1) != 0);
    }
    } P;

    Bus* bus;

  public:
    Cpu(){};
    Cpu(Bus*);

    int cycle();
    uint8_t* addrCallType(int);
    void setJK();
    uint16_t combineJK();
};
